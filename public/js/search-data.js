// Statistiques de recherche (optionnel, pour analytics)
window.searchStats = {
  totalSearches: 0,
  popularSearches: [],

  logSearch: function (query) {
    this.totalSearches++;

    // Garder les 10 recherches les plus populaires
    const existing = this.popularSearches.find((s) => s.query === query);
    if (existing) {
      existing.count++;
    } else {
      this.popularSearches.push({ query, count: 1 });
    }

    // Trier par popularité
    this.popularSearches.sort((a, b) => b.count - a.count);
    this.popularSearches = this.popularSearches.slice(0, 10);
  },

  getPopularSearches: function () {
    return this.popularSearches;
  },
};
